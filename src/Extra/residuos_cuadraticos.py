while True:
    try:
        n = int(input())
        aux = []
        for i in range(1, n):
            aux.append((i*i) % n)
        aux = list(set(aux))
        aux.sort()
        for i in range(len(aux)-1):
            print(aux[i], end=" ")
        print(aux[-1])
    except EOFError:
        break


